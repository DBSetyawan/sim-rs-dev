                        <div class="article-content">
                            <div class="article-body">
                                <p>Yes, we do! Just like our boxes, we offer fully customized paperboard or corrugated
                                    cardboard inserts to fit inside any custom boxes you order from us. The current
                                    minimum for custom inserts is 2000 units if you're starting from scratch and need
                                    assistance with creating a dieline file (template) for the insert itself.&nbsp;</p>
                                <p>If your project's quantity meets the applicable minimum, you can email our Packlane
                                    Plus team at&nbsp;<a
                                        href="mailto:quotes@packlane.com">quotes@packlane.com</a>&nbsp;to get started
                                    today! As a heads up, weâ€™ll need the below information for quoting:</p>
                                <ul>
                                    <li>
                                        <p>Size of box and insert (dimensions in inches)</p>
                                    </li>
                                    <li>
                                        <p>Preliminary or final version of the box artwork, and insert artwork (if
                                            printing on the insert)</p>
                                    </li>
                                    <li>
                                        <p>Preferred material - kraft corrugate, white corrugate, or white paperboard
                                        </p>
                                    </li>
                                    <li>
                                        <p>Order quantity (or quantities)</p>
                                    </li>
                                </ul>
                                <p>If you don't have a dieline/template file ready, but you're looking for a simple
                                    corrugated pad or divider for your boxes, we may be able to help for a reduced
                                    minimum order quantity. Please send your project's details along with a sample image
                                    of the pad/divider you're looking for over to quotes@packlane.com for our team's
                                    review.</p>
                                <p><em>All of our inserts are custom-made to order, just like our boxes. We don't
                                        currently offer ready-made inserts or dividers.&nbsp;</em></p>
