                    <section class="article-info">
                        <div class="article-content">
                            <div class="article-body">
                                <p><strong><em>*Please note that our HDPrintGloss offerings now have a minimum order
                                            quantity of 15 units.</em></strong></p>
                                <p>Yes, you can place a small test order of 1-10 custom&nbsp;<a
                                        href="https://packlane.com/customize/mailer-box" target="_blank"
                                        rel="noreferrer noopener">Mailer</a>, <a
                                        href="https://packlane.com/customize/shipping-box" target="_blank"
                                        rel="noreferrer noopener">Shipper</a>, or <a
                                        href="https://packlane.com/customize/shipping-box?quantity=100&amp;sub_type=econoflex"
                                        target="_blank" rel="noopener">Econoflex</a> boxes to review your particular box
                                    size and design before committing to a larger order.&nbsp;</p>
                                <p>This small test order will follow the same production turnaround as a regular order,
                                    with available expedited shipping and Rush production options (for eligible orders).
                                    The estimated delivery date of your test order can be previewed at Checkout once
                                    your order options are selected and a shipping address is entered.&nbsp;</p>
                                <p>For our paperboard&nbsp;<a href="https://packlane.com/customize/folding-carton"
                                        target="_blank" rel="noreferrer noopener">Product Boxes</a>, the minimum order
                                    quantity is currently 25 units. We’re regrettably unable to produce a smaller custom
                                    test order quantity in this style.</p>
                                <p>Don’t have time for a custom test order? Our<a
                                        href="https://packlane.com/samples?utm_source=internal&amp;utm_medium=portal"
                                        target="_blank" rel="noreferrer noopener">&nbsp;sample kit</a>&nbsp;can be used
                                    to review our material options, box structures, and digital print quality ahead of
                                    submitting your larger order.</p>
                                <p>For help with sizing, we highly recommend checking out our&nbsp;<a
                                        href="https://l.packlane.com/custom-size-packaging/?utm_source=customer_support&amp;utm_medium=email"
                                        target="_blank" rel="noopener noreferrer">sizing page here</a>! We've added some
                                    helpful tips on the process, along with a calculator to provide you with recommended
                                    dimensions based on a Roomy or Snug fit around your product(s).</p>
                                <p>&nbsp;</p>
