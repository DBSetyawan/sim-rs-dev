                                <h3>When you have your artwork ready on your dieline template,&nbsp;<a
                                        href="{{ route('dieline_design.id') }}"
                                        target="_blank" rel="noreferrer noopener"><u><strong>please submit your finished
                                                dieline file here</strong></u></a>&nbsp;(.AI .PDF or .EPS).</h3>
                                <p>We'll do a quick review of the file for any standout concerns and ink coverage before
                                    sending you a quote and custom checkout link to submit your order. :)</p>
                                <p>After your order is placed, our Prepress Team will review your artwork more
                                    thoroughly and send a 2D proof with any print quality concerns they may find.&nbsp;
                                </p>
                                <p><em>Please be advised: Non-technical concerns such as spelling errors, RGB or
                                        PMS-to-CMYK color shifts, and atypical design orientation are not reviewed in
                                        our proofing process. If you have any concerns about your file set-up, please
                                        reach out to our&nbsp;</em><a href="https://packlane.com/contact" target="_self"
                                        rel="noreferrer noopener">Support team</a><em>&nbsp;and we'll be happy to help
                                        before you submit your order.&nbsp;</em></p>
                                <p><strong>Don't forget to review any applicable&nbsp;<a
                                            href="/hc/en-us/articles/360045523951" target="_blank"
                                            rel="noopener">Artwork Guidelines</a>&nbsp;before submitting your files for
                                        printing!</strong></p>
