                        <div class="article-content">
                            <div class="article-body">
                                <p><span><strong>Designing and ordering on the 3D design tool</strong></span><br>If you
                                    have individual artwork elements such as logos, images, or text, you can customize
                                    dimensions and specifications for your box directly on our&nbsp;<a
                                        href="https://www.box.new/" target="_blank" rel="noopener"><strong>free 3D box
                                            designer</strong></a>&nbsp;and receive pricing on that same page! The design
                                    tool updates the price in real-time to account for changes to ink coverage and box
                                    size as you build your design.</p>
                                <p><em>Please make sure not to note pricing until your design is complete.</em></p>
                                <p>We've created some videos to help you get started if you choose to design on the box
                                    designer :)</p>
                                <p>How to use the design tool for the Mailer box:&nbsp;<a
                                        href="https://cl.ly/0E3E3X1Q3d3x" target="_blank"
                                        rel="noreferrer noopener"><strong><u>Watch Mailer Video</u></strong></a></p>
                                <p>How to use the design tool for the Shipping box:&nbsp;<a
                                        href="https://cl.ly/210W0s3D1G3a" target="_blank"
                                        rel="noreferrer noopener"><strong><u>Watch Shipper Video</u></strong></a></p>
                                <p>How to use the design tool for the Product Box: <a href="https://cl.ly/2M1U0P1A0K0L"
                                        target="_blank" rel="noreferrer noopener"><strong><u>Watch Product Box
                                                Video</u></strong></a><br>When your design is finished, you can proceed
                                    to Checkout directly from the design page, being sure to select or enter your
                                    quantity before moving forward to Checkout.<br><br><span><strong>Designing and
                                            ordering with a 2D dieline (template)</strong></span><br>If your design is
                                    too complex to build panel by panel on our 3D design tool, or you prefer to work in
                                    a professional program like Adobe Illustrator, you can build your design on a 2D
                                    dieline template! Simply <a href="http://packlane.com/dieline-request"
                                        target="_blank" rel="noreferrer noopener">request a dieline here</a>&nbsp;to get
                                    started and then head over to our&nbsp;<a
                                        href="https://packlane.com/dieline-upload?utm_source=customer_support&amp;utm_medium=email"
                                        target="_blank" rel="noopener noreferrer"><strong><u>Dieline
                                                Upload</u></strong></a>&nbsp;tool to request a quote once your file is
                                    ready to go.</p>
                                <p>&nbsp;</p>
