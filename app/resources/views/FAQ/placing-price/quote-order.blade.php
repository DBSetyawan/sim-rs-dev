                    <section class="article-info">
                        <div class="article-content">
                            <div class="article-body">
                                <p>As you customize your box on the box designer page, you will see the price per unit
                                    update in real time. On that page, you will select the size, material, quantity, and
                                    box style. &nbsp;As you upload artwork or add text and select background coloring,
                                    our handy calculator tells you exactly how those choices affect the price.</p>
                                <ul>
                                    <li><a href="https://packlane.com/customize/mailer-box?utm_source=internal&amp;utm_medium=portal"
                                            rel="noreferrer noopener">Mailer Box</a></li>
                                    <li><a href="https://packlane.com/customize/shipping-box?utm_source=internal&amp;utm_medium=portal"
                                            rel="noreferrer noopener">Shipper Box</a></li>
                                    <li><a href="https://packlane.com/customize/classic-carton?utm_source=internal&amp;utm_medium=portal"
                                            target="_self" rel="undefined">Product Box</a></li>
                                </ul>
                                <p><strong><em>Please note: the price you see on the online design tool may not account
                                            for ink costs if placing an order with a dieline.</em></strong>&nbsp;To
                                    receive a quote for an order with artwork placed on a dieline template,
                                    please&nbsp;<a href="/hc/en-us/articles/360045494811" target="_blank"
                                        rel="noopener">click here</a>&nbsp;for instructions.&nbsp;</p>
                                <p>The box designer is great for mailer or shipper orders up to 2000 units, or for
                                    product box orders up to 3000 units. If you need a larger quantity, we're happy to
                                    provide quotes for bulk orders through our Packlane Plus team! <a
                                        href="https://packlane.com/request-quote" target="_blank" rel="noopener">Click
                                        here</a>&nbsp;to submit your quote request. :)&nbsp;</p>
