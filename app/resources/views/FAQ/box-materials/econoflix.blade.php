                    <section class="article-info">
                        <div class="article-content">
                            <div class="article-body">
                                <p><strong>Econoflex</strong> is our first response to one of our most popular customer
                                    requests: <strong>a fully recyclable, budget-friendly, and environmentally conscious
                                        Shipping box solution!</strong></p>
                                <p>Our innovative Econoflex print technology achieves crisp, fine lines with half-tone
                                    designs, comparable to Flexographic printing. The boxes themselves are made using
                                    sturdy, 100% recycled <em>and</em> recyclable Kraft corrugated cardboard. All at an
                                    economical price point!</p>
                                <p><strong>A few things to note about ordering Econoflex:</strong></p>
                                <ul>
                                    <li>Minimum order quantity is just 1 box, perfect for prototyping and *bulk
                                        ordering!</li>
                                    <li>Currently available in a selection of common Shipper sizes</li>
                                    <li>Mirrored 2-panel customization for a full wraparound print (see options
                                        <strong><a href="https://share.getcloudapp.com/JruWnjWQ">here</a></strong>)</li>
                                    <li>All designs print in black ink with the help of half-tones to achieve lighter
                                        and darker shades<br><span>*<em>Quantities of 1 - 10,000 boxes can be quoted and
                                                ordered instantly on our <a
                                                    href="https://packlane.com/customize/shipping-box?sub_type=econoflex"
                                                    target="_blank" rel="noopener noreferrer"><u>Econoflex box
                                                        page</u></a>. For larger quantities, you can&nbsp;</em><a
                                                href="https://packlane.com/contact" target="_blank"
                                                rel="noopener noreferrer">contact our team</a><em>. Quantities of more
                                                than 20,000 units can be requested through our&nbsp;</em><a
                                                href="https://packlane.com/request-quote?utm_source=customer_support&amp;utm_medium=email"
                                                target="_blank" rel="noopener noreferrer"><u><em>Packlane Plus
                                                        team</em></u></a></span><em><span>.</span></em></li>
                                </ul>
                                <p><strong>What is a half-tone, you may ask?</strong></p>
                                <p>Half-toning is a print technique that breaks up an image into a series of dots with
                                    various sizes and spacing to reproduce a full tone range in artwork without using a
                                    gradient. Our Econoflex box designer automatically converts your artwork into a
                                    single-color half-tone so you don't have to worry about figuring it out yourself!
                                    100% black elements will print solid black while any gray or lower opacity elements
                                    will be converted into half-tone dots.<br><em>Youâ€™ll immediately see the half-tone
                                        result in the 3D preview and you'll have another chance to review and approve
                                        your design during proofing.&nbsp;</em><em>For best results with half-tone
                                        printing, please see our <a href="/hc/en-us/articles/360045090092"
                                            target="_blank" rel="noopener">Guidelines</a> here.</em></p>
                                <p>In the up-close photo below, you can see the varying shades of black/gray that make
                                    up the image of the dog and bone are created using variably sized and spaced dots of
                                    ink (this is half-tone). Solid design elements, like the 100% black text, are
                                    printed as solid shapes for a crisp high-quality print.&nbsp;</p>
                                <p><a href="https://share.getcloudapp.com/DOu86ej6" target="_blank"
                                        rel="noopener noreferrer"><img class="fr-fic fr-fil fr-dib"
                                            src="/hc/article_attachments/360055666191/5ea3690dbffe8.png"></a></p>
                                <p>At a distance, those individual half-tone printed dots in the grayscale image come
                                    together to form an optically solid print!</p>
                                <p><a href="https://share.getcloudapp.com/d5u06lRv" target="_blank"
                                        rel="noopener noreferrer"><img class="fr-fic fr-fil fr-dib"
                                            src="/hc/article_attachments/360055534932/5ea3690fc84dd.png"></a></p>
