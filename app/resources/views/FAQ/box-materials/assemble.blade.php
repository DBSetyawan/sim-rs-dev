                    <section class="article-info">
                        <div class="article-content">
                            <div class="article-body">
                                <p>Unsure of how to assemble your corrugated boxes or just want to make sure you're
                                    doing it right? Watch this video, it helps!</p>
                                <p>&nbsp;</p>
                                <p><iframe src="https://player.vimeo.com/video/227435409" width="640" height="360"
                                        frameborder="0" allowfullscreen=""></iframe></p>
                                <p><a href="https://vimeo.com/packlane/assemble-a-mailer-box">How to assemble a mailer
                                        box</a>&nbsp;from&nbsp;<a
                                        href="https://vimeo.com/packlane">packlane</a>&nbsp;on&nbsp;<a
                                        href="https://vimeo.com/">Vimeo</a>.</p>
                                <p>&nbsp;</p>
                                <p><strong><em>Prefer step-by-step instructions?&nbsp;</em></strong>These should help
                                    you get a great result:</p>
                                <p>&nbsp;</p>
                                <p>1) Clear off a clean, flat work surface. Ideally, the surface should be larger than
                                    your un-assembled box sheet, so the sheet can sit flat and you're not bumping into
                                    other objects as you build the box. <em>For best results, do not layer the boxes
                                        while folding as this can damage the boxes underneath and also result in bad
                                        creases in the box you're working on.&nbsp;</em></p>
                                <p>2) Notice the <em>scores</em> (pre-fold lines) on the interior side of your
                                    unassembled box sheet. One-by-one, fold over each of the scores
                                    <u>completely&nbsp;</u><u>towards the interior</u>, then release it back to lay flat
                                    on the table, interior facing up. This is called creasing, or "breaking" the folding
                                    points.</p>
                                <p><strong>Please note:</strong> On the sidewall there are two scores that are very
                                    close together. It is <strong>essential</strong> that you take care to fold each of
                                    these creases separately. Otherwise, the wall will be too short on the inside and
                                    the sidewall will not lock into the bottom.</p>
                                <p><img src="/hc/article_attachments/360089595992/03-fold-creases-separately-double-fold.png"
                                        alt="03-fold-creases-separately-double-fold.png" width="416" height="339"></p>
                                <p>3) Fold up the front and back flaps, positioning the tuck flaps along the side of the
                                    box as much as possible. Brace the back in place lightly as you complete the next
                                    step.</p>
                                <p><img src="/hc/article_attachments/360089596112/05-fold-up-front-and-back.png"
                                        alt="05-fold-up-front-and-back.png" width="494" height="346"></p>
                                <p>4) Bring up the side walls of your box by folding the left and right side flaps over
                                    the tuck flaps, then&nbsp;<u>snap them into place at the same moment</u>. This
                                    ensures a very symmetrical, even structure where the lock tabs are equally snug.</p>
                                <p><img src="/hc/article_attachments/360089596132/06-bring-up-side-walls.png"
                                        alt="06-bring-up-side-walls.png" width="393" height="314"></p>
                                <p><img src="/hc/article_attachments/360089596472/07-snap-sides-into-place.png"
                                        alt="07-snap-sides-into-place.png" width="396" height="301"></p>
                                <p><strong>The key visual to know it's right is when the tops of the side walls are
                                        flat, not slanted or peaked.</strong></p>
                                <p><img src="/hc/article_attachments/360089596452/08-check-top-of-side-wall.png"
                                        alt="08-check-top-of-side-wall.png" width="478" height="363"></p>
                                <p>5) You can now add your contents, close the lid and slide the locking tabs into their
                                    slots in the sidewall. The result should be a sturdy, locked box.&nbsp;</p>
                                <p>If something feels off or the box is not closing smoothly, please reach out to us and
                                    we'll be happy to help troubleshoot!</p>
                            </div>

