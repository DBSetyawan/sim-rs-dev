                        <div class="article-content">
                            <div class="article-body">
                                <p><span>We care passionately about sustainability! The majority of our cardboard
                                        material options contain recycled content, to the highest degree allowed by the
                                        paper industry's standards. Kraft and Standard White corrugated cardboard both
                                        contain a majority of recycled material, while others - such as our SBS
                                        paperboard Product Boxes and kemi board Dreamcoat - contain less due to their
                                        manufacturing.</span></p>
                                <p><strong><a href="https://packlane.com/blog/tips-for-sustainable-packaging"
                                            target="_blank" rel="noopener"><span>Top 11 Tips to Make Your Packaging More
                                                Sustainable</span></a></strong></p>
                                <p><span>Our boxes are also printed in North America (USA and Canada) using materials
                                        sourced within each local region, resulting in a much lower carbon footprint
                                        than manufacturers who source raw or finished materials from overseas.</span>
                                </p>
                                <p><span>All of our unprinted box materials are fully recyclable.</span></p>
