                        <div class="article-content">
                            <div class="article-body">
                                <p><strong><em>*Please note that our HDPrintGloss offerings now have a minimum order
                                            quantity of 15 units.</em></strong></p>
                                <p>Kind of! We currently have a <em><strong>glossy print
                                            finis</strong><strong>h</strong></em> option available on our Dreamcoat
                                    material. :)</p>
                                <p>This means that unprinted (white) areas on your box will remain the original texture
                                    of the material itself, while printed (ink) areas will be cured to a high-shine
                                    gloss for a striking contrast! Deep and dark colors produce the highest level of
                                    gloss with this option. Pastels and lighter colors result in a more subtle gloss or
                                    nearly-matte effect. In the example images below, the box on the right is showcasing
                                    our Glossy finish option.</p>
                                <p>To order glossy boxes you can go to our <a
                                        href="https://packlane.com/customize/mailer-box?utm_source=customer_support&amp;utm_medium=email"
                                        target="_blank" rel="noopener noreferrer">Mailer Box</a> page, set your box
                                    size, select Dreamcoat from the Material menu, and then select <strong>HDPrint
                                        Gloss</strong> in the Print Area Finish section.&nbsp;</p>
