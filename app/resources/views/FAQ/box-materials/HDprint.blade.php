                        <div class="article-content">
                            <div class="article-body">
                                <p><strong><em>*Please note that our HDPrintGloss offerings now have a minimum order
                                            quantity of 15 units.</em></strong></p>
                                <p>HDPrint is our latest and greatest print option! With the help of new print
                                    technology, we're excited to provide you with this new, high-quality print finish
                                    option on our Dreamcoat material. Some benefits of HDPrint printing include:</p>
                                <ul>
                                    <li>Smaller ink dots! And smaller ink dots = finer linework. Small text, thin lines,
                                        and detailed images can print better than ever with HDPrint <em>(compared to
                                            standard digital printing).</em></li>
                                    <li>Printed surface is smoother than our non-HDPrint products, resulting in a print
                                        appearance that is comparable to more expensive, high-volume manufacturing
                                        methods.</li>
                                    <li>Little to no odor! Depending on the design, the printed ink will have very low
                                        or zero odor on your finished box.</li>
                                    <li>HDPrint's single-pass printing process is “greener” than our regular printing
                                        option, which means less waste and less consumables used.</li>
                                </ul>
                                <p>When combined with our Dreamcoat material, HDPrint printing results in a glossy print
                                    finish wherever ink is present. This means that unprinted (white) areas on your box
                                    will remain the original texture of the material itself, while printed (ink) areas
                                    will be cured to a high-shine gloss for a striking contrast! Deep and dark colors
                                    produce the highest level of gloss with this option. Pastels and lighter colors
                                    result in a more subtle gloss or nearly-matte effect.&nbsp;</p>
                                <p><em>HDPrint is currently limited to Dreamcoat material Mailer boxes, but stay tuned
                                        for future releases on our other material options!</em></p>
                                <p>To order glossy HDPrint boxes you can go to our <a
                                        href="https://packlane.com/customize/mailer-box?utm_source=customer_support&amp;utm_medium=email"
                                        target="_blank" rel="noopener noreferrer"><u>Mailer Box</u></a> page, select or
                                    set your box size, select Dreamcoat from the Material menu, and then click the Gloss
                                    option in the Print Area Finish section.&nbsp;</p>
